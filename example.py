import datetime
# to print date and time at now
print(datetime.datetime.now())
# to print max year
print(datetime.MAXYEAR)
# to print min year
print(datetime.MINYEAR)

# to get date
# datetime.date(year,month,day)
print(datetime.date(2004,3,12))

# to get time
# datetime.time(hour,minute,second,microsecond,and tzinfo)
print(datetime.time(5,45,23))

print(datetime.datetime.now())
